DROP TABLE books;
DROP TABLE BOOKSHELVES;

CREATE TABLE IF NOT EXISTS books (
  id SERIAL PRIMARY KEY,
  author VARCHAR(255),
  title VARCHAR(255),
  isbn VARCHAR(255) UNIQUE,
  image_url VARCHAR(255),
  description TEXT,
  bookshelf_id  INT
);

CREATE TABLE IF NOT EXISTS BOOKSHELVES (
  id SERIAL PRIMARY KEY,
  name VARCHAR(255) UNIQUE
  );